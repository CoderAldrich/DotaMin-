public class AbilityUpgrades {

    private int ability;
    private int time;
    private int level;


    public void setAbility(int ability) {
        this.ability = ability;
    }
    public int getAbility() {
        return ability;
    }


    public void setTime(int time) {
        this.time = time;
    }
    public int getTime() {
        return time;
    }


    public void setLevel(int level) {
        this.level = level;
    }
    public int getLevel() {
        return level;
    }
}
